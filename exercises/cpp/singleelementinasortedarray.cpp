class Solution {
public:
    int singleNonDuplicate(vector<int>& nums) 
    {
        // O(log n) time, O(1) space 
        int lo = 0; 
        int hi = nums.size();
        while (lo <= hi)
        {
            int mid = lo + (hi - lo) / 2;
            
        }    
    }
};
